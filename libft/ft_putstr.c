#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;
	int	len;

	i = 0;
	while (str[i])
		i++;
	len = i;
	i = 0;
	while (i < len)
	{
		write(1, &str[i], 1);
		i++;
	}
}
